#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*str;

	if (!s1)
		return (NULL);
	start = 0;
	end = ft_strlen(s1) - 1;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	if (start < end)
	{
		while (s1[end] && ft_strchr(set, s1[end]))
			end--;
	}
	str = ft_substr(s1, start, end - start + 1);
	return (str);
}
